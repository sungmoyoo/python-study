#덧셈
def add(a,b):
    result = (a+b)
    return result

#뺄셈
def sub(a,b):
    result = (a-b)
    return result

#곱셈
def mul(a,b):
    result = (a*b)
    return result

#나눗셈
def div(a,b):
    result = (a/b)
    return result

#결과 확인
print(add(20,10))
print(sub(20,10))
print(mul(20,10))
print(div(20,10))